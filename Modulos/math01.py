import math

# Acessar o número PI
print(math.pi)

# Acessar o número de Euler
print(math.e)

# Arrendondar um número para cima e para baixo
num = 10.4
print(math.ceil(num))
print(math.floor(num))

# Fatorail de um número
num2 = int(input("Digite um número: "))
print(math.factorial(num2))

# Potência de um número
print(math.pow(2, 3))

# Raiz quadrada de um número
print(math.sqrt(169))

# MDC  - Máximo Divisor Comum
mdc = math.gcd(20, 100)
print(mdc)

# Logaritmo de um número
print(math.log(10))